package day5_unary_assignment_relation_operators;

public class task18_preAndPostDecrementIncrement {

	public static void main(String[] args) {

		int x = 2;
		int y = x++;// same line ==> y=2
		System.out.println(x);// after line ==> x=3
		System.out.println(y++);// same line y = 2
		System.out.println(--y);// after line y =2
		
		
		
		
		
		
	}

}
